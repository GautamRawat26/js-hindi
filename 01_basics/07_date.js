//Dates

let myDate = new Date();
console.log(myDate); // 2025-03-16T15:35:30.334Z
console.log(myDate.toDateString());//Sun Mar 16 2025
console.log(myDate.toString());//Sun Mar 16 2025 15:35:30 GMT+0000 (Coordinated Universal Time)
console.log(myDate.toISOString());//2025-03-16T15:35:30.334Z
console.log(myDate.toLocaleDateString());// 3/16/2025


